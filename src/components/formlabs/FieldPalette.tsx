interface FieldPaletteProps {
  onAddField: (type: FieldType) => void;
}

export default function FieldPalette({ onAddField }: FieldPaletteProps) {
  const handleDragStart = (e: React.DragEvent, type: FieldType) => {
    e.dataTransfer.setData("text/plain", type);
    e.dataTransfer.effectAllowed = "copy";
  };

  return (
    <div className="h-full flex flex-col">
      <div className="px-4 py-3 border-b border-white/[0.04]">
        <h3 className="text-xs font-semibold text-gray-300 tracking-wide uppercase">
          Fields
        </h3>
        <p className="text-[10px] text-gray-600 mt-0.5">
          Drag to add
        </p>
      </div>
      <div className="flex-1 overflow-y-auto p-2 space-y-1">
        {fieldTypes.map((ft) => (
          <button
            key={ft.type}
            draggable
            onDragStart={(e) => handleDragStart(e, ft.type)}
            onClick={() => onAddField(ft.type)}
            className="w-full flex items-center gap-2.5 px-3 py-2.5 text-sm text-gray-400 hover:text-gray-200 hover:bg-white/[0.04] rounded-lg transition-all duration-200 active:scale-[0.97] cursor-grab active:cursor-grabbing border border-transparent hover:border-white/[0.06]"
          >
            <span className="text-gray-500 shrink-0">{ft.icon}</span>
            <span>{ft.label}</span>
          </button>
        ))}
      </div>
    </div>
  );
}
